public class ArrayBasics {
    public static void main(String args []){
        int[] x = {4,52,88,7736,98,110};
        // indexing
        System.out.println(x[3]);
        // update
        x[3] = 171;
        System.out.println(x[3]);

        int[] arr = new  int[4];
        arr[0] = 202;
        arr[1] = 226;
        arr[2] = 222;
        arr[3] = -22;
        System.out.println(arr[2]);
    }
}
